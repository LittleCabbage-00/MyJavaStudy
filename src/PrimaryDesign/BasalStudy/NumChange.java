package PrimaryDesign.BasalStudy;

public class NumChange {
    public static void main(String[] args){
        double above = 0.7D;
        double below = 0.4D;
        float fabove = 0.7F;
        float fbelow = 0.4F;
        System.out.println("(int)above: " + (int)above);
        System.out.println("(int)below: " + (int)below);
        System.out.println("(int)fabove: " + (int)fabove);
        System.out.println("(int)fbelow: " + (int)fbelow);
        System.out.println("Math above: " + Math.round(above));
        System.out.println("Math below: " + Math.round(below));
        System.out.println("Math fabove: " + Math.round(fabove));
        System.out.println("Math fbelow: " + Math.round(fbelow));
}
}
